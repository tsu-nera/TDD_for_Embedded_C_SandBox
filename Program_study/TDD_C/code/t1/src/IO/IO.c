/* START: full */
#include "IO.h"

/* START: IO_Write */
void IO_Write(ioAddress addr, ioData data)
{
    ioData * p = 0;
    *(p + addr) = data;
}
/* END: IO_Write */

/* START: IO_Read */
ioData IO_Read(ioAddress addr)
{
    ioData * p = 0;
    return *(p + addr);
}
/* END: IO_Read */
